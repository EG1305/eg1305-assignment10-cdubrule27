
public class Rectangle {
double length;
double width;

public Rectangle(double a,double b) {
	length=a;
	width=b;
}

	public double getArea(double a, double b) {
		double x = a*b;
		return x;
		
	}
    public double  getPerimeter(double a,double b) {
    	double z= a+a+b+b;
    	return z;
    	 
    }
    
}

